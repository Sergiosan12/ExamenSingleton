public class Record {
    /**
     * alias del record
     */
    private String aliasR;
    /**
     * puntuacion record
     */
    private int puntuacionR;

    public String getAlias() {
        return aliasR;
    }

    public void setAlias(String alias) {
        this.aliasR = alias;
    }

    public int getPuntuacion() {
        return puntuacionR;
    }

    public void setPuntuacion(int puntuacion) {
        this.puntuacionR = puntuacion;
    }
}
